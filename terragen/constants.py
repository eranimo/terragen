from PIL import ImageColor

def hsl2rgb(c):
    return ImageColor.getrgb(c)

def L(n):
    if n > 255:
        return 255
    if n < 0:
        return 0
    return n

def lighten(c):
    am = 15
    return (L(c[0] + am), L(c[1] + am), L(c[2] + am))

